    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String phy    = jTextField2.getText();
        String chem   = jTextField3.getText();
        String mat    = jTextField4.getText();
        String eng    = jTextField5.getText();
        String elec   = jTextField6.getText();
         try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/eas","root","Easwar@29");
            Statement st =con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("update result set rollno = '"+rollno+"' ,phy ='"+phy+"' ,chemistry = '"+chem+"',maths ='"+mat+"' ,eng = '"+eng+"',elec = '"+elec+"' where rollno = '"+rollno+"'");
                JOptionPane.showMessageDialog(null, "Successfully updated result");
                setVisible(false);
                new Update().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Roll no does not exist");
            }
             }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        
         }
    }//GEN-LAST:event_jButton7ActionPerformed

